/*
百马百担问题：有100匹马，驮100担货，大马驮3担，中马驮2担，两匹小马驮1担，问大，中，小马各有多少？

思路:穷举法，用三个for循环分别代表大马，中马和小马的数量
*/

#include<stdio.h>

int main(){
    int a,b,c;
    for(a=0;a<=100;a++){
        for(b=0;b<=100;b++){
            //注意，小马的数量必须为双数
            for(c=0;c<=100;c+=2){
                if(a+b+c==100 && 3*a+2*b+c/2==100){
                    printf("num:L %d, M %d, S %d\n", a,b,c);
                }
            }
        }
    }
    return 0;
}